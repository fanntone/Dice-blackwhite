let lang = (function(){ try{ const s=localStorage.getItem('rhds_lang'); if(s && I18N[s]) return s; }catch(e){} return (navigator.language||'').toLowerCase().indexOf('zh')===0 ? 'zh-Hant' : 'en'; })();
function t(key, params){
  let s = (I18N[lang] && I18N[lang][key]); if(s==null) s = (I18N.en && I18N.en[key]); if(s==null) s = key;
  if(params) for(const k in params) s = s.split('{'+k+'}').join(params[k]);
  return s;
}
function updateLangBtn(){ const b=document.getElementById('langbtn'); if(b) b.textContent = '🌐 ' + ((I18N[lang]&&I18N[lang]['lang.name'])||lang); }
function applyStaticI18n(){
  document.documentElement.lang = (lang==='zh-Hant') ? 'zh-Hant' : 'en';
  document.querySelectorAll('[data-i18n]').forEach(el=> el.textContent = t(el.getAttribute('data-i18n')));
  document.querySelectorAll('[data-i18n-html]').forEach(el=> el.innerHTML = t(el.getAttribute('data-i18n-html')));
  document.querySelectorAll('[data-i18n-ph]').forEach(el=> el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph'))));
  updateLangBtn();
}
function setLang(l){ if(!I18N[l]) return; lang=l; try{ localStorage.setItem('rhds_lang', l); }catch(e){} applyStaticI18n(); if(view) render(); }
